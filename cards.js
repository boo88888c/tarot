function makeInterpretation(name, suit, core, uprightKeywords, reversedKeywords) {
  const context = SUIT_CONTEXT[suit];
  return {
    name,
    suit,
    uprightKeywords,
    reversedKeywords,
    coreTheme: core,
    general: `${name}的核心是「${core}」。${context.general}`,
    love: `${context.love}這張牌會把焦點放在「${core}」這件事上。`,
    career: `${context.career}它提醒你用「${core}」來看現在的處境。`,
    money: `${context.money}這張牌會把「${core}」變成很實際的提醒。`,
    relationship: `${context.relationship}你可以觀察「${core}」是否正在你們之間發生。`,
    advice: `先抓住「${uprightKeywords[0]}」這個方向，再決定下一步，不必一次把所有事做滿。`,
    warning: `小心落入「${reversedKeywords[0]}」的狀態，尤其不要用焦慮替自己做決定。`,
    spokenLines: [
      `這張${name}比較像是在說：事情的重點不是表面那一層，而是${core}。`,
      `它給你的提醒很直接，先看見${uprightKeywords[0]}，再處理${reversedKeywords[0]}。`,
      `如果你心裡已經有答案，這張牌是在提醒你把感覺落到行動上。`
    ]
  };
}

function majorCard(seed) {
  const [id, name, suit, core, uprightKeywords, reversedKeywords] = seed;
  return { id, ...makeInterpretation(name, suit, core, uprightKeywords, reversedKeywords) };
}

function suitedCards(group) {
  return group.titles.map((name, index) => ({
    id: `${group.suit}-${index + 1}`,
    ...makeInterpretation(name, group.suit, group.cores[index], group.uprights[index], group.reverseds[index])
  }));
}

export const TAROT_CARDS = [
  ...MAJORS.map(majorCard),
  ...SUITS.flatMap(suitedCards)
];
